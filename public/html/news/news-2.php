    <h2>BLD announces Cubert Academy, a Counter-Strike Academy team for the 2025 season</h2>
    <p>Published on: 2023-12-15</p>
    <p>Author: BLD e-Sports</p>
    <img src="../images/news/cubert-academy.png" alt="BLD announces Cubert Academy, a Counter-Strike Academy team for the 2025 season">
    <div class="news-card">
        <div class="news-card-text">
            <h1>Los Angeles, California</h1>
            <p>– North American organization **BLD** is thrilled to unveil its **Counter-Strike Academy** roster for the 2025 season. Featuring **Zaki “Reduxx” Dilan** as the first core member, **Cubert Academy** is set to make waves in the competitive **Counter-Strike** scene.</p>

            <p>“Our vision is simple but impactful. First, we aim to provide a nurturing environment for up-and-coming talent who aren’t yet eligible for premier **CS:GO** competitions, offering them a platform to evolve. At the same time, we want to create opportunities for seasoned players to showcase their skills, positioning them as strong candidates for joining top-tier **CS:GO** teams in the future, including our own **BLD** roster,” said **Mohamad F**, CEO of **BLD**.</p>

            <p>While **Cubert Academy** won’t be competing in the **Pro League** just yet, contracts will be structured to give experienced players the option to transition to any top-tier **CS:GO** organization mid-season. After that point, a predetermined buyout of $10k or less will be available, ensuring they can pursue new opportunities without the usual high buyout hurdles.</p>

            <p>This initiative will allow players to gain crucial experience in the **Challengers Circuit** and get scouted by major **CS:GO** teams, including **BLD**, without the usual high buyout hurdles.</p>

            <p>**Cubert Academy** will be the only North American **Counter-Strike** academy team with such an arrangement. Fans can keep up to date with all team news and updates by following the official **@CubertAcademy** Twitter account.</p>

            <p>Support the team directly by grabbing exclusive **Cubert Collection** merch, available in the **BLD Store**.</p>


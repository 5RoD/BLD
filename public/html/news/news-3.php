    <h2>BLD announces gaming chair sponsorship with Razer</h2>
    <p>Published on: 2024-06-12</p>
    <p>Author: BLD e-Sports</p>
    <img src="../images/news/BLD-PARTNERSHIP-razer.png" alt="BLD announces gaming chair sponsorship with Razer">
    <div class="news-card">
        <div class="news-card-text">
            <h1>Los Angeles, California</h1>
            <p>– North American organization **BLD** is proud to announce a year-long partnership with **Razer**, the world’s leading lifestyle brand for gamers. With this partnership, the **Razer Iskur V2** gaming chair becomes the official chair for **BLD** players.</p>

            <p>The **Razer Iskur V2** is built for ergonomic comfort, with its 6D Adjustable and Adaptive Lumbar Support System designed to fit the natural curvature of the spine. It also offers a custom-tailored experience with adjustable lumbar protrusion and height, ensuring optimal comfort. The chair also features high-density foam cushions, a reactive seat tilt, and up to 152-degree recline functionality.</p>

            <p>Speaking on the partnership, CEO **Mohamad F** said:</p>
            <p>“We’re excited to partner with **Razer**, a brand that has been a long-time leader in the esports industry. This partnership ensures that our players will have the most comfortable and innovative gaming chairs, helping them perform at their highest level. With the **Razer Iskur V2**, we’re confident our team will have the perfect equipment to compete in the most intense **Counter-Strike** matches.”</p>

            <p>**BLD** is now officially part of **Team Razer**, alongside some of the world’s best esports athletes. This announcement follows the success of **Zaki “Reduxx” Dilan** in securing victories with the **Razer Viper V3 Pro** wireless esports mouse in **Counter-Strike** tournaments.</p>

            <p>Fans can look forward to exclusive **Razer Iskur V2** chairs and other **Razer** products available in the **BLD Store**. With this partnership, **BLD** players will now have top-tier gaming peripherals and ergonomic support to keep them at the top of their game.</p>


        <h2>BLD named as Esports World Cup Foundation Partner</h2>
        <p>Published on: 2023-10-01</p>
        <p>Author: BLD e-Sports</p>
        <img src="../images/news/worldcuppartner.jpg" alt="BLD named as Esports World Cup Foundation Partner">
        <div class="news-card">
            <div class="news-card-text">
                <h1>Los Angeles, California</h1>
                <p>– Today, BLD was named as one of the 40 teams selected to become a partner for the Esports World Cup Foundation.</p>

                <p>The EWCF Club Partnership is an industry-leading initiative designed to promote the sustainable growth of esports organizations. The $20 million program offers participating Clubs up to $1 million in funding to expand their brand and grow their global audience through innovative content and marketing campaigns leading up to and throughout the Esports World Cup 2025.</p>

                <p>The CEO of BLD, Mohamad F, said:</p>
                <p>“Today, as many of you have seen, BLD was announced as a partnered team with the Esports World Cup Foundation, the organizing body of the Esports World Cup.</p>
                <p>This opportunity not only provides substantial resources to help expand our brand, but it also enables us to join some of the world's most recognized teams and further diversify our competitive portfolio—extending our reach beyond Counter-Strike and into other titles.</p>
                <p>BLD will be seeking players or teams looking to become part of the organization in time for the Esports World Cup 2025. BLD will aim to sign one to two additional teams. Interested parties can reach out to VP of Operations Mohamad F: 2158177@talnet.nl or via Discord (@5RoD).</p>
                <p>On a personal note, I want to ensure this brand becomes one of the biggest in the world, so we will continue to expand. This partnership is one of the very few in esports that allows that reality."</p>

                <p><strong>About BLD</strong></p>
                <p>BLD is a premiere Los Angeles-based esports organization that fields competitive teams in Counter-Strike and other titles. The organization has a wide range of content creators, including former Counter-Strike major winner Tarik Celik, and notable streamers like Daphne “39Daph” Wai and Jared “Zombs” Gitlin.</p>
                <p>BLD Academy is the Counter-Strike team for BLD. We are committed to providing a platform for these talented players to showcase their skills and grow toward international recognition. This academy team is designed to create a development pipeline for future BLD stars.</p>
                <p>BLD aims to build competitive teams across esports while connecting as many passionate fans as possible into our network. We want to give them access to our players and content and develop a strong, connected community around esports.</p>
                <p>From 2022 to 2024, BLD became one of the fastest-growing esports organizations on Twitter, YouTube, and Instagram in North America.</p>

